package cianom.lib;


public class IntPoint {

    private final int x;
    private final int y;

    public IntPoint(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getW() {
        return this.x;
    }

    public int getH() {
        return this.y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int getFirst() {
        return this.x;
    }

    public int getSecond() {
        return this.y;
    }

}
